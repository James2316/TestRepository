package dataTypes;

public class WhileLoop {

	public static void main(String[] args) {
		
		// this is an example of a control statement
		String place = "Brooklyn";
		int start = 1;
		
		while(start<=3)
		{
			System.out.println("I am from " + place);
			start++;
		}
		
		
		

	}

}
